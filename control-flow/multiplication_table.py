# multiplication_table.py
# ده نسخة أسهل وأبسط لبرنامج جدول الضرب باستخدام for loop

# 1. بنطلب من اليوزر يدخل الرقم اللي عايز يشوف جدول الضرب بتاعه
# بنستخدم int() عشان نحول اللي دخله اليوزر لرقم صحيح
# هنا بنفترض إن اليوزر هيدخل رقم صح دايماً عشان نسهل الكود عليك
number = int(input("Enter a number to see its multiplication table: "))

# 2. بنستخدم for loop عشان نلف من 1 لـ 10
# range(1, 11) معناها ابدأ العد من 1 لحد 10 (الرقم اللي بعد الـ 10 هو آخرنا)
# كل مرة الـ loop تلف، المتغير 'i' هياخد قيمة جديدة (1، 2، 3، وهكذا لحد 10).
for i in range(1, 11):
    # 3. بنحسب حاصل الضرب
    product = number * i

    # 4. بنطبع السطر بتاع جدول الضرب بالصيغة المطلوبة مباشرةً
    # بنستخدم f-string عشان نحط قيم المتغيرات جوه النص بسهولة
    print(f"{number} * {i} = {product}")