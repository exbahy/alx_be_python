# ده نسخة أسهل وأبسط لبرنامج رسم الأنماط باستخدام while و for loops

# 1. بنطلب من اليوزر يدخل حجم النمط (طول وعرض المربع)
# بنستخدم int() عشان نحول اللي دخله اليوزر لرقم صحيح
# هنا بنفترض إن اليوزر هيدخل رقم صحيح موجب دايماً عشان نسهل الكود عليك
size = int(input("Enter the size of the pattern: "))

# 2. بنرسم المربع باستخدام while loop للصفوف و for loop للأعمدة

# بنعرف متغير عشان يعد الصفوف اللي طبعناها
# هنبدأ من الصفر (أول صف)
row_count = 0

# الحلقة الخارجية (while loop): دي بتلف عدد مرات يساوي الـ 'size' عشان ترسم كل صف
# هتفضل شغالة طول ما رقم الصف اللي احنا فيه (row_count) أقل من الحجم اللي اليوزر دخله
while row_count < size:
    # الحلقة الداخلية (for loop): دي بتلف على كل "نجمة" في الصف الواحد
    # for _ in range(size): معناها: كرر الأمر اللي جوايا 'size' مرة (عدد النجوم في الصف)
    # الـ "_" بنستخدمها هنا لما نكون مش محتاجين نستخدم قيمة المتغير في اللفة (مجرد بنكرر عدد معين من المرات)
    for _ in range(size):
        # بنطبع النجمة "*".
        # `end=""` دي مهمة جداً! معناها: "بعد ما تطبع النجمة، متنزليش سطر جديد، خلي المؤشر في نفس السطر."
        print("*", end="")
    
    # بعد ما الـ for loop تخلص طباعة كل النجوم في الصف الحالي (اللي هما 'size' نجمة)
    # بنعمل print() فاضية عشان ننزل سطر جديد ونبدأ نرسم الصف اللي بعده
    print() 
    
    # بنزود عداد الصفوف واحد عشان ننتقل للصف اللي بعده في الـ while loop الجاية
    row_count += 1